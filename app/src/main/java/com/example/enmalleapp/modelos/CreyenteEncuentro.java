    public String getApellidosEncuentro() {
        return apellidosEncuentro;
    }

    public void setApellidosEncuentro(String apellidosEncuentro) {
        this.apellidosEncuentro = apellidosEncuentro;
    }

    public String getEdadEncuentro() {
        return edadEncuentro;
    }

    public void setEdadEncuentro(String edadEncuentro) {
        this.edadEncuentro = edadEncuentro;
    }

    public int getImageDeleteEncuentro() {
        return imageDeleteEncuentro;
    }

    public void setImageDeleteEncuentro(int imageDeleteEncuentro) {
        this.imageDeleteEncuentro = imageDeleteEncuentro;
    }
