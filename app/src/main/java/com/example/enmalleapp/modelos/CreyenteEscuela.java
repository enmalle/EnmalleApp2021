package com.example.enmalleapp.modelos;

public class CreyenteEscuela {

    private String idCreyenteEscuela;
    private String nombresEscuela;
    private String apellidosEscuela;
    private String edadEscuela;
    private int imageDeleteEscuela;

    public CreyenteEscuela() {
        this.idCreyenteEscuela = idCreyenteEscuela;
        this.nombresEscuela = nombresEscuela;
        this.apellidosEscuela = apellidosEscuela;
        this.edadEscuela = edadEscuela;
        this.imageDeleteEscuela = imageDeleteEscuela;
    }



    public String getIdCreyenteEscuela() {
        return idCreyenteEscuela;
    }

    public void setIdCreyenteEscuela(String idCreyenteEscuela) {
        this.idCreyenteEscuela = idCreyenteEscuela;
    }

    public String getNombresEscuela() {
        return nombresEscuela;
    }

    public void setNombresEscuela(String nombresEscuela) {
        this.nombresEscuela = nombresEscuela;
    }

    public String getApellidosEscuela() {
        return apellidosEscuela;
    }

    public void setApellidosEscuela(String apellidosEscuela) {
        this.apellidosEscuela = apellidosEscuela;
    }

    public String getEdadEscuela() {
        return edadEscuela;
    }

    public void setEdadEscuela(String edadEscuela) {
        this.edadEscuela = edadEscuela;
    }

    public int getImageDeleteEscuela() {
        return imageDeleteEscuela;
    }

    public void setImageDeleteEscuela(int imageDeleteEscuela) {
        this.imageDeleteEscuela = imageDeleteEscuela;
    }
}
